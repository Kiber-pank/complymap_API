// Импортируем конфигурацию dotenv. Это должно быть самой первой строкой в точке входа.
// Он читает файл .env и добавляет переменные в process.env до выполнения остального кода.
import 'dotenv/config';
import { buildApp } from './app';
import { env } from './config/env';

// Асинхронная функция запуска сервера.
// Использование async/await необходимо, так как инициализация плагинов и открытие порта требуют времени.
async function start() {
  // Собираем экземпляр Fastify со всеми зарегистрированными плагинами и маршрутами.
  const app = await buildApp();
  try {
    // Запускаем HTTP-сервер на указанном порту и интерфейсе.
    // host: '0.0.0.0' означает, что сервер будет доступен не только с localhost, но и по внешнему IP.
    // Это важно для запуска в облачных средах и виртуальных машинах.
    await app.listen({ port: env.PORT, host: '0.0.0.0' });

    // Логируем успешный запуск. Встроенный логгер автоматически добавляет метку времени и уровень.
    app.log.info(`Server running on http://localhost:${env.PORT}`);
    app.log.info(`Swagger UI: http://localhost:${env.PORT}/docs`);
  } catch (err) {
    // Если порт занят или произошла критическая ошибка инициализации, логируем её.
    app.log.error(err);
    // Завершаем процесс с кодом 1 (ошибка), чтобы оркестраторы или системные службы поняли, что сервис не запустился.
    process.exit(1);
  }
}

// Вызываем функцию запуска немедленно после загрузки модуля.
start();